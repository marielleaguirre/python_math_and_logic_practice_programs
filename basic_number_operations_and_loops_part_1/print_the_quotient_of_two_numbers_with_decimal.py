# Prog05: Create a program that ask user to input 2 numbers. Print the quotient of the two numbers with the decimal point

'''
    ask the user to input first_number and second_number
    print the quotient of the two numbers with decimal point
'''
# ask the user to input first_number and second_number
first_number = float(input("Enter the first number: "))
second_number = float(input("Enter the second number: "))

# print the quotient of the two numbers with decimal point
print("The quotient of", first_number, "and", second_number, "with decimal point is", first_number / second_number)