# Prog04: Create a program that ask user to input 2 numbers. Print the product of the two numbers.

'''
    ask the user to input the first_number and second_number
    print the product of the two numbers
'''
# ask the user to input the first_number and second_number
first_number = float(input("Enter the first number: "))
second_number = float(input("Enter the second number: "))

# print the product of the two numbers
print("The product of", first_number, "and", second_number, "is", first_number * second_number)